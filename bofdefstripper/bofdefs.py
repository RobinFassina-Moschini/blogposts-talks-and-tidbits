import argparse
import os.path
import re

bofdef_defined_functions = []
def parse_bofdefs(filePath):
    if os.path.exists(filePath):
        bofdef_template = open(filePath, 'r')
        lines = bofdef_template.readlines()
        for line in lines:
            match = re.search(f'\$(.*?)\(',line)
            if match:
                # debug purposes - print(str(match.group(1)) +'\n')
                bofdef_defined_functions.append(match.group(1))
        bofdef_template.close()
    else:
        raise FileNotFoundError( filePath + " does not seem to exist \n")

def parse_bofsource(filePath):
    bofdef_keywords = []
    if os.path.exists(filePath):
        source_code = open(filePath,'r')
        lines = source_code.readlines()
        for line in lines:
            for keyword in bofdef_defined_functions:
                if keyword in line:
                    bofdef_keywords.append(keyword)
        source_code.close()
        return bofdef_keywords
    else:
        raise FileNotFoundError( filePath + " does not seem to exist \n")

def generate_stripped_bofdefs(filePath,outPath,keywords):
     if keywords:
        if os.path.exists(filePath):
            bofdef_template = open(filePath, 'r')
            stripped_bofdefs = open(outPath,'w')
            stripped_bofdefs.write(f"#pragma once\n")
            stripped_bofdefs.write(f"#pragma intrinsic(memcmp, memcpy,strcpy,strcmp,strlen)\n")
            stripped_bofdefs.write(f"#include <windows.h>\n")
            stripped_bofdefs.write(f"\n")
            lines = bofdef_template.readlines()
            for line in lines:
                for keyword in keywords:
                    if keyword in line:
                        if "#" in line:
                            pass
                        else:
                            stripped_bofdefs.write(line)
            bofdef_template.close()
            stripped_bofdefs.close()

